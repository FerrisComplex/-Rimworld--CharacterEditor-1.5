    internal static void UpdateLabels()
    {
        try
        {
            if (LanguageDatabase.activeLanguage == null)
                currentLanguage = "english";
            else if (currentLanguage != LanguageDatabase.activeLanguage.FriendlyNameEnglish.ToLower())
                currentLanguage = LanguageDatabase.activeLanguage.FriendlyNameEnglish.ToLower();
            Log.Message("updating labels for " + currentLanguage + "...");
            if (currentLanguage.StartsWith("german"))
                LangDE();
            else if (currentLanguage.StartsWith("russian"))
                LangRU();
            else if (currentLanguage.StartsWith("french"))
                LangFR();
            else if (currentLanguage.StartsWith("japanese"))
                LangJP();
            else if (currentLanguage.StartsWith("simplified chinese"))
                LangCN();
            else if (currentLanguage.StartsWith("polish"))
                LangPO();
            else if (currentLanguage.StartsWith("spanish"))
                LangSP();
            else
                LangEN();
            CEditor.ListName = COLONISTS;
        }
        catch (Exception ex)
        {
            Log.Error(ex.StackTrace);
        }
    }

    internal static void AddNames(string rootDir)
    {
        AddNamesFromPath(rootDir + Path.DirectorySeparatorChar + "Defs" + Path.DirectorySeparatorChar + "FemaleNames.txt", (GenderPossibility)1);
        AddNamesFromPath(rootDir + Path.DirectorySeparatorChar + "Defs" + Path.DirectorySeparatorChar + "MaleNames.txt", 0);
    }

    private static void AddNamesFromPath(string path, GenderPossibility gp)
    {
        try
        {
            foreach (var str in FileIO.ReadFile(path).AsStringUNICODE().SplitNo(";"))
            {
                var strArray = str.Replace("\n", "").Replace("\r", "").Replace("\t", "").Trim().SplitNo(",");
                if (strArray.Length >= 3)
                    PawnNameDatabaseSolid.AddPlayerContentName(new NameTriple(strArray[0], strArray[1].NullOrEmpty() ? strArray[0] : strArray[1], strArray[2]), gp);
            }
        }
        catch
        {
        }
    }
